#Archivo con definiciones de funciones

#MODULOS

#Funcion para multiplicar dos numeros

def prod(a,b):
    return a*b

#Funcion para restar dos numeros

def resta(a,b):
    return a-b

#Funcion para sumar dos numeros

def suma(a,b):
    return a+b

#Funcion para dividir dos numeros

def division(a,b):
    return a/b
